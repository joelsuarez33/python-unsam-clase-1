cadena = "geringoso"
for c in cadena:
    if c == "a":
        cadena = cadena.replace("a", "apa")
        
    elif c == "e":
        cadena = cadena.replace("e", "epe")
        
    elif c == "i":
        cadena = cadena.replace("i", "ipi")
        
    elif c == "o":
        cadena = cadena.replace("o", "opo")
        break
    
print(cadena)
        


    
